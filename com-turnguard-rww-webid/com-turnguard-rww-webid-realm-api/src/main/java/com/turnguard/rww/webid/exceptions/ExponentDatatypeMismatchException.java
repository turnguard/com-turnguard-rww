public class ExponentDatatypeMismatchException extends Exception {

    public ExponentDatatypeMismatchException() {
    }

    public ExponentDatatypeMismatchException(String message) {
        super(message);
    }

    public ExponentDatatypeMismatchException(String message, Throwable cause) {
        super(message, cause);
    }

    public ExponentDatatypeMismatchException(Throwable cause) {
        super(cause);
    }

    public ExponentDatatypeMismatchException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
