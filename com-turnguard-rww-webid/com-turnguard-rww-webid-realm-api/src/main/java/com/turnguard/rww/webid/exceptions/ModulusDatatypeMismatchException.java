package com.turnguard.rww.webid.exceptions;

/**
 *
 * @author {@link <a href="http://www.turnguard.com/turnguard" target="_blank">turnguard</a>}
 */
public class ModulusDatatypeMismatchException extends Exception {

    public ModulusDatatypeMismatchException() {
    }

    public ModulusDatatypeMismatchException(String message) {
        super(message);
    }

    public ModulusDatatypeMismatchException(String message, Throwable cause) {
        super(message, cause);
    }

    public ModulusDatatypeMismatchException(Throwable cause) {
        super(cause);
    }

    public ModulusDatatypeMismatchException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
